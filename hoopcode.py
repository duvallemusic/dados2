def print_hoopcode_ball():
    ORANGE = "\033[38;5;208m"  # Laranja (256 cores)
    BLACK = "\033[30m"         # Preto
    RESET = "\033[0m"

    art = [
        "              <HOOP CODE>          ",
        "                    1",
        "            00000000100000000",
        "        1000000000001000000000001",
        "      00001000000000100000000010000",
        "    000000001000000010000000100000000",
        "   00000000001000000100000010000000000",
        "  0000000000001000001000010000000000000",
        " 000000000000000100010001000000000000000",
        " 000000000000000100010001000000000000000",
        " 000000000000000100010001000000000000000",
        "11111111111111111111111111111111111111111",
        " 000000000000000100010001000000000000000",
        " 000000000000000100010001000000000000000",
        " 00000000000000010001000100000000000000",
        "  0000000000000100001000001000000000000",
        "   00000000001000000100000010000000000",
        "    000000001000000010000000100000000",
        "      00001000000000100000000010000",
        "        1000000000001000000000001",
        "            00000000100000000",
        "                    1"
    ]

    for line in art:
        colored_line = ""
        for char in line:
            if char == "0":
                colored_line += ORANGE + "0" + RESET
            elif char == "1":
                colored_line += BLACK + "1" + RESET
            else:
                colored_line += char
        print(colored_line)

print_hoopcode_ball()
